				Core i7-7700HQ (Kaby Lake) adalah penerus i7-6700HQ (Skylake), dimana ini merupakan CPU quad-core mobile, yang berjalan di banyak game dan notebook multimedia yang kuat. Chip ini diproduksi dalam proses 14 nm dan memiliki TDP 45 Watt dengan berbagai clock dasar 2,8 dan boost hingga 3,8 GHz.

				Dalam tes benchmark yang dilakukan,  prosesor Core i7-7700HQ lebih cepat sekitar 4-11% dari pada core i7-6700HQ sebelumnya, sementara kita juga bisa melihat keuntungan dari 8- 9% dalam Test multi. Ini kira-kira setara dengan perbaikan yang kita lihat dengan perubahan sebelumnya dari generasi CPU. Secara keseluruhan, chip ini sedikit lebih cepat, tetapi tidak perlu untuk upgrade, terutama karena notebook gaming masih dibatasi oleh kinerja grafis.</p>

