				Asus ROG Strix GL753VD rata-rata cukup antara notebook gaming lainnya dalam hal ukuran dan berat. Sebagai contoh, sistem high-end seperti G752 misalnya, jauh lebih berat dan lebih tebal dari GL753, sementara sistem seperti MSI GS73 atau Asus GL702, yang lebih menekankan pada mobilitas, bahkan lebih ringan dan lebih tipis pada khususnya. Hal ini biasanya dicapai dengan menghilangkan optical drive.</p>

